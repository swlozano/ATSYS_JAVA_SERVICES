package VO;

/**
 *
 * @author Alejandro
 */
public class ValorFijoPsVO {
    private String concepto;
    private double valor;
    private double retencion;
    private double valorRetencion;

    /**
     * @return the concepto
     */
    public String getConcepto() {
        return concepto;
    }

    /**
     * @param concepto the concepto to set
     */
    public void setConcepto(String concepto) {
        this.concepto = concepto;
    }

    /**
     * @return the valor
     */
    public double getValor() {
        return valor;
    }

    /**
     * @param valor the valor to set
     */
    public void setValor(double valor) {
        this.valor = valor;
    }

    /**
     * @return the retencion
     */
    public double getRetencion() {
        return retencion;
    }

    /**
     * @param retencion the retencion to set
     */
    public void setRetencion(double retencion) {
        this.retencion = retencion;
    }

    /**
     * @return the valorRetencion
     */
    public double getValorRetencion() {
        return valorRetencion;
    }

    /**
     * @param valorRetencion the valorRetencion to set
     */
    public void setValorRetencion(double valorRetencion) {
        this.valorRetencion = valorRetencion;
    }

}
